package org.model;

public class DatosSeleccionExamen {

	private String idMensaje;

	public DatosSeleccionExamen() {
		super();
	}

	public DatosSeleccionExamen(String idMensaje) {
		super();
		this.idMensaje = idMensaje;
	}

	public String getIdMensaje() {
		return idMensaje;
	}

	public void setIdMensaje(String idMensaje) {
		this.idMensaje = idMensaje;
	}
	
	
	
}
